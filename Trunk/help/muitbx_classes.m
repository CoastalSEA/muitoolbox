% <matlab:doc('muidataui') muiDataUI>: defines a UI for data selection that is implemented to suit specific uses (see some of the utlity classes below that make use of _muiDataUI_). <br>
% <matlab:doc('muidataset') muiDataSet>: defines methods to manipulate datasets and is implemented by clasess for imported data and models (see _muiUserData_ and _muiUserModel_ below).  <br>

