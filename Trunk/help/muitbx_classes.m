%% muitoolbox classes
% The muitoolbox comprises a number of classes that collectively allow
% bespoke User Interfaces (UIs) to be rapidly built and customised.

%% muitoolbox abstract classes 
% muitoolbox classes include the following abstract classes:
%%
% <matlab:doc('muimodelui') muiModelUI>: defines a basic stucture for a modelling UI, with methods to initialise menus and tabs. <br>
% <matlab:doc('muipropertyui') muiPropertyUI>: defines a default interface, with methods to handle and display model parameter input. <br>
% <matlab:doc('muidataui') muiDataUI>: defines a UI for data selection that is implemented to suit specific uses (see some of the utlity classes below that make use of _muiDataUI_). <br>
% <matlab:doc('muidataset') muiDataSet>: defines methods to manipulate datasets and is implemented by clasess for imported data and models (see _muiUserData_ and _muiUserModel_ below).  <br>
%
%% muitoolbox utility classes
% muitoolbox classes include the following utility classes:
%%
% <matlab:doc('muicatalogue') muiCatalogue>: implements the data catalogue to hold access imported data and model outputs.  <br>
% <matlab:doc('muiproject') muiProject>: holds the current project details. <br>
% <matlab:doc('muiconstants') muiConstants>: holds standard physical values (acceleration due to gravity, densities, year to seconds, etc). <br>
% <matlab:doc('muiuserdata') muiUserData>: import data sets using user-defined format file.  <br>
% <matlab:doc('muieditui') muiEditUI>: data selection UI to edit data sets.  <br>
% <matlab:doc('muiplotsui') muiPlotsUI>: data selection UI to generate plots.  <br>
% <matlab:doc('muiplots') muiPlots>: methods for plot options based on UI selection. <br>
% <matlab:doc('muistatsui') muiStatsUI>: data selection UI for statistical anlysis. <br>
% <matlab:doc('muistats') muiStats>: methods for statistical analysis based on UI selectio.  <br>
% <matlab:doc('muimanipui') muiManipUI>: data selection UI to define an equation or function call.  <br>
% <matlab:doc('muiusermodel') muiUserModel>: methods to evaluate functions based on UI selection.  <br>

