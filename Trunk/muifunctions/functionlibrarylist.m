         'userderivedoutput(t,x,y,z,flag)';...
         'z.*repmat(1,1,length(x),length(y))';...
         'z.*repmat(1,length(t)),length(x),length(y))';...
         '[Nan;diff(x,n,dim)]';...
         '[diff(x,n,dim);NaN]'};
 
